cadena = 'Este es un ejemplo de Substring (Debanado de cadenas)'

print(len(cadena))

print(cadena[2])

print(cadena[0 : 10]) #Muestra la cadena hasta el caracter antes del de la posicion 10
print(cadena[ : 10]) #Muestra la cadena hasta el caracter antes del de la posicion 10

print(cadena[2 : ]) #Muestra la cadena hasta el final

print(cadena[-1]) #Muestra la cadena hasta el final